/* -----------------------------------------------------------------------------
	include
----------------------------------------------------------------------------- */
#include	"Log.h"

#include	"Queue.h"


/* -----------------------------------------------------------------------------
	Local Macro
----------------------------------------------------------------------------- */



/* -----------------------------------------------------------------------------
	local Structure
----------------------------------------------------------------------------- */



/* -----------------------------------------------------------------------------
	Local Variable
----------------------------------------------------------------------------- */
static queue_t	q_speed = NEW_QUEUE;
static queue_t	q_current = NEW_QUEUE;



/* -----------------------------------------------------------------------------
	local function
----------------------------------------------------------------------------- */



/* -----------------------------------------------------------------------------
	private function
----------------------------------------------------------------------------- */



/* -----------------------------------------------------------------------------
	public function
----------------------------------------------------------------------------- */
void Log_Write(int16_t speed, int16_t current)
{
	Enqueue(&q_speed, speed);
	Enqueue(&q_current, current);
}


int16_t Log_ReadSpeed(void)
{
	int16_t data;
	Dequeue(&q_speed, &data);
	return data;
}


int16_t Log_ReadCurrent(void)
{
	int16_t data;
	Dequeue(&q_current, &data);
	return data;
}