uint8_t GPIO_ReadHall(void)
{
	uint8_t hall = 0;
	
	hall |= ( HAL_GPIO_ReadPin(Hall_U_GPIO_Port, Hall_U_Pin)==GPIO_PIN_SET ) << 0;
	hall |= ( HAL_GPIO_ReadPin(Hall_V_GPIO_Port, Hall_V_Pin)==GPIO_PIN_SET ) << 1;
	hall |= ( HAL_GPIO_ReadPin(Hall_W_GPIO_Port, Hall_W_Pin)==GPIO_PIN_SET ) << 2;
	
	return hall;
}